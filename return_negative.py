# return negative number
# for any given number
# some numbers can be negative

# P - a real number
# R - a negative number or 0 
# E - 0 -> 0, 1 -> -1, -0.89 -> -0.89, 45 -> -45
# P - find absolute value and then return negative absolute value or absolute * -1

def make_negative( number ):
    return -abs(number)
